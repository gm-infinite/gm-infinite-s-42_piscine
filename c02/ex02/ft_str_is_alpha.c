int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i])
	{
		if (((str[i] > 'z' || str[i] < 'a') \
		&& (str[i] > 'Z' || str[i] < 'A')))
			return (0);
		i++;
	}
	return (1);
}
